    private void btnGerarBoletimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGerarBoletimActionPerformed
        String nome, result;
        double n1, n2, n3, media;
        
        DecimalFormat i = new DecimalFormat("0");
        DecimalFormat f = new DecimalFormat("0.0");
                
        nome = txtNomeAluno.getText();
        n1 = Double.parseDouble(txtNota1.getText());
        n2 = Double.parseDouble(txtNota2.getText());
        n3 = Double.parseDouble(txtNota3.getText());
        media = (n1 + n2 + n3) / 3;
        txtNomeBol.setText(nome);
        txtNota1Bol.setText(String.valueOf(f.format(n1)));
        txtNota2Bol.setText(String.valueOf(f.format(n2)));
        txtNota3Bol.setText(String.valueOf(f.format(n3)));
        txtMediaBol.setText(String.valueOf(f.format(media)));
        if(media >= 7){
            result = "APROVADO";
        } else {
            result = "REPROVADO";
        }
        lbTextPane.setText("O(A) Aluno(a) " + nome + " estÃ¡ " + result + ", com mÃ©dia " + f.format(media) + ".");
    }//GEN-LAST:event_btnGerarBoletimActionPerformed

